# Сборка полного системного промпта

def build_system_prompt(lang: str = "ru") -> str:
    """
    Собирает полный системный промпт из XML-структурированных секций.

    Args:
        lang: 'ru' (русский) или 'kk' (казахский). При 'kk' добавляются
              казахские инструкции и few-shot примеры.

    Порядок секций по приоритету (критичные — в начале):
    1. Роль + Границы (кто ты, что можешь/не можешь)
    2. Правила (жёсткие ограничения)
    3. Anti-injection (защита)
    4. Reasoning instructions (как думать — CoT)
    5. Инструменты (что доступно)
    6. Few-shot примеры (как применять — ReAct trace)
    6.5 [KK] Казахские инструкции + few-shot (если lang='kk')
    7. Формат ответа + Self-check (как отвечать)
    8. Error handling (как обрабатывать ошибки)
    9. Схема БД (справочная информация — в конце)
    """
    sections = [
        SYSTEM_ROLE,
        BOUNDARIES,
        RULES,
        ANTI_INJECTION,
        REASONING_INSTRUCTIONS,
        TOOLS_DESCRIPTION,
        FEW_SHOT_EXAMPLES,
    ]

    if lang == "kk":
        sections.append(KAZAKH_LANGUAGE_INSTRUCTIONS)
        sections.append(KAZAKH_FEW_SHOT)

    sections.extend([
        RESPONSE_FORMAT,
        SELF_CHECK,
        ERROR_HANDLING,
        DB_SCHEMA,
    ])

    return "\n\n".join(sections)


def get_question_hint(question_type: str) -> str:
    """Возвращает стратегию-подсказку для конкретного типа вопроса."""
    return QUESTION_TYPE_HINTS.get(question_type, "")
